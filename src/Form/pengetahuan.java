    private void jTextField_searchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_searchKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()== KeyEvent.VK_ENTER)
        {
            DftTblModel = new DefaultTableModel();
            DftTblModel.addColumn("ID");
            DftTblModel.addColumn("NAMA KERUSAKAN");
            DftTblModel.addColumn("JENIS MOBIL");
            DftTblModel.addColumn("GEJALA");
            DftTblModel.addColumn("NILAI GEJALA");
            jTable1.setModel(DftTblModel);
            java.sql.Connection conn = new Koneksi.koneksi().connect();     
            try{         
                java.sql.Statement stmt = conn.createStatement();
                String SQL = " select*from gejala where gejala like '%"+jTextField_search.getText()+"%' or jenismobil like '%"+jTextField_search.getText()+"%'";
                java.sql.ResultSet res=stmt.executeQuery(SQL);
                while(res.next()){
                DftTblModel.addRow(new Object[]{
                res.getString("Id"),
                res.getString("nama"),
                res.getString("jenismobil"),
                res.getString("gejala"),
                res.getString("nilai")                              
                });         
                }         
                jButton_updategejala.setEnabled(true);                    
                jButton_simpan.setEnabled(false);
     
            }catch(Exception e){             
            }    
        }
        else{
            TampilData();
        }
    }//GEN-LAST:event_jTextField_searchKeyPressed


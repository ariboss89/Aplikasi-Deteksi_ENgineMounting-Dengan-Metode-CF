    public void id(){        
        java.sql.Connection conn= new Koneksi.koneksi().connect();       
        try {            
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select *from gejala ORDER BY Id DESC"); 
            if (rs.next()) {
                String nocust = rs.getString("Id").substring(4);
                String AN = "" + (Integer.parseInt(nocust) + 1);
                String Nol = "";
                if(AN.length()==1)
                {Nol = "000";}
                else if(AN.length()==2)
                {Nol = "00";}
                else if(AN.length()==3)
                {Nol = "0";}
                else if(AN.length()==4)
                {Nol = "";}               
                jLabel_ID.setText("GJL-" + Nol + AN);
            }else{
                jLabel_ID.setText("GJL-0001");
            }           
        }catch(Exception e){           
            JOptionPane.showMessageDialog(null, e);           
        }     
    }
    

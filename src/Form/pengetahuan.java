    private void jButton_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_simpanActionPerformed
        // TODO add your handling code here:
        if(jTextField_gejala.getText().equals("")){
            JOptionPane.showMessageDialog(null, "GEJALA KOSONG !!!");
            jTextField_gejala.requestFocus();
        }
        else if(jTextField_nilai.getText().equals("")){
            JOptionPane.showMessageDialog(null, "NILAI KOSONG !!!");
            jTextField_nilai.requestFocus();
        }
        else{
            java.sql.Connection conn= new Koneksi.koneksi().connect();            
        try{
            java.sql.PreparedStatement stmt=conn.prepareStatement("insert into gejala (Id,nama,jenismobil,gejala,nilai) values (?,?,?,?,?)");
            try{           
                stmt.setString(1,jLabel_ID.getText()); 
                stmt.setString(2,jTextField_namakerusakan.getText());
                stmt.setString(3,jComboBox_jenismobil.getSelectedItem().toString());
                stmt.setString(4,jTextField_gejala.getText());
                stmt.setString(5,jTextField_nilai.getText());               
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null,"MENYIMPAN DATA","Pesan",JOptionPane.INFORMATION_MESSAGE);
                refresh();
               
            } catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"GAGAL MENYIMPAN DATA !!!","Pesan",JOptionPane.WARNING_MESSAGE);
            }
        }catch(Exception e){

        }
        }        
    }//GEN-LAST:event_jButton_simpanActionPerformed

